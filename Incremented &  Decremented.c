#include <conio.h>
int main() {
    int X ;


    printf("Enter the value of : ");
    scanf("%d", &X);


    printf("Incremented ++X: %d\n", ++X);


    printf("Decremented --X: %d\n", --X);


    printf("Incremented X++: %d\n", X++);

    printf("Decremented X--: %d\n", X--);

    getch ();
}


